import jsonschema
import pytest

import pagent.config


def test_default_config():
    """Check default config content.

    Check that default config stored in app source passes the schema validation.
    """
    conf = pagent.config.file.load_default()
    jsonschema.validate(conf, pagent.config.schemas.CONFIG)
